		public static bool validWord(string word)
		{
			if (s_kNotAllowWords.Contains(word)) return false;

			if (s_kAllowWords.Contains(word)) return true;

			if (s_kEnds.Contains(word)) return true;

			for (int sepa = 0; sepa <= word.Length - 2; sepa++)
			{
				string begin = word.Substring(0, sepa + 1);
				string end = word.Substring(sepa + 1);

				if (s_kBegins.Contains(begin) && s_kEnds.Contains(end))
				{
					string k = begin + "-" + end[0];
					if (!s_kNotAllowPairs.Contains(k))
					{
						return true;
					}
				}
			}

			return false;
		}

