package com.android.gl2jni;

import android.util.Log;

public class K {
	public static final String TAG = "GAME";
	
	public static void logi(String text) {
		Log.i(TAG, text);
	}
	
	public static void loge(String text) {
		Log.e(TAG, text);
	}
	
	public static void logd(String text) {
		Log.d(TAG, text);
	}
}
