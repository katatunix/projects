		<p>As a student at UoG you have access to central IT facilities, including the Online Timetable, Electronic Library Services, email, Portico, and Moodle. You need to have a UoG userid and password to access these services.</p>

		<p>You will receive your UoG userid and set your password prior to pre-enrolment.</p>

		<p>You will need to keep your password updated, both for security reasons and because it will expire every 150 days. You will be notified via your UoG email 30 days before your password is set to expire. You can change your password at any time using MyAccount. You will not be allowed to reuse the same password or similar ones. You can use MyAccount to also check your storage quota, printing credit, and more.</p>

		<p>We recommend you register with the User Authentication Service. This service allows you to obtain a new password in the event that you forget your current one or if it expires, without having to go in person to the ISD Service Desk.</p>

		<p>During enrolment every student will receive an information card about student IT services, covering email, internet connections in halls, wireless, security, training and remote desktop services. If you do not receive this card, you can collect one from the ISD Service Desk in the DMS Watson Science Library. The card also contains a map showing where all the main computer rooms are, and details on how to contact the ISD Service Desk. There are also a number of online resources to help you to get started. The Information Services Division has a dedicated page for new students. There is also an online guide to using the Online Timetable.</p>

		<p>General IT queries should be directed to the ISD Service Desk which is located on the ground floor of the DMS Watson Science Library in Malet Place (map ref C5). The ISD Service Desk is open from 9.30am to 5pm Monday to Friday. The ISD Service Desk can also be contacted by calling 0207 679 5000 (Monday to Friday, 8.30am - 5.30pm) or emailing <a href="mailto:servicedesk@uog.ac.uk">servicedesk@uog.ac.uk</a>.</p>

		<p><b>Moodle</b></p>

		<p>Moodle is UoG's online teaching and learning environment where you can take part in activities and read resources as part of your studies. This is an excellent way to keep in touch with your department and other students on your course, and to get help with academic work.</p>

		<p>This resource is used in a variety of ways across UoG:
