		<p><b>When and where do I enrol?</b></p>

		<p>Once you have completed pre-enrolment you will be sent an email telling you when and where to enrol. If you fail to complete pre-enrolment or don't receive any communication about these arrangements before you are due to arrive please contact the New Students Team. All students are required to enrol in person within the two weeks from their official start date.</p>

		<p>Enrolment for new students takes place in the Student Centre, which is located in the Chadwick Building on the main UoG campus (see the Maps page for details). The Student Centre is open Monday to Friday from 10am to 4pm unless otherwise advertised.</p>

		<p><b>What do I need to bring with me?</b></p>

		<p>All students should make sure they are able to produce their Student Reference Number on request. You will find your Student Reference Number on your offer letter from UoG. Please bring a copy of your offer letter with you when you come to enrol.</p>

		<p>You will be asked to provide:

		<div class="mylist">
			* An accepted form of photographic identification. UoG's preferred form of identification is a valid passport. If you do not have a valid passport the following will be accepted forms of identification for UK, EU/EEA and Swiss students - a UK/EU photo driving licence (a valid provisional licence is acceptable), a European National Identity Card, Armed Forces Identity Card, an original birth certificate, adoption certificate or certificate of naturalisation.</br /></br />
			* If you are coming to study at UoG from outside of the EU/EEA/Switzerland, the presentation of a valid passport and visa to study in the UK is a mandatory requirement. You will need to provide evidence that you have permission to be in the UK that has not expired, does not prohibit study and does not prohibit study at UoG. Information on whether you require a visa to study in the UK, how to apply, guidance notes and application forms are available from the GOV.UK website. UoG is not permitted to allow students to enrol without the correct documentation so please make sure that you have all the required documentation before you travel to UoG.</br /></br />
			* If you are registering at UoG while an application for a visa is in process of being considered in the UK, you must ensure that you have copies of your passport details page and your previous leave to remain in the UK and bring with you any acknowledgement that you have from the Home Office of your application. Without this, we cannot enrol you.</br /></br />
			* If you are a non EU/EEA or Swiss citizen you might need an ATAS certificate. Please see the International Student Support page for information and which departments require you to have an ATAS certificate.</br /></br />
			* If you have not yet done so, you will also need to pay your tuition fees, or provide evidence of a sponsor if you have not already done this (see the Pay Your Fees page for more information).</br />
		</div>
		</p>

		<p><b>Statement of Student Status</b></p>

		<p>When you have completed enrolment, you will be given a Statement of Student Status. This is an official document confirming that you are a UoG student. You will present this statement to obtain your UoG ID card and can also use this as evidence to open a student bank account and claim council tax exemption (full time students only). It is essential that your home, contact and term-time addresses are up to date so that the correct information will appear on this letter.</p>

